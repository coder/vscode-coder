#!/usr/bin/env node

// Captures the exact CSS custom properties and default webview stylesheet
// that a real VS Code instance injects into webviews, for each built-in
// theme, and writes them to .storybook/themes/generated/.
//
// It builds a minimal fixture extension in a temp dir and launches VS Code
// via @vscode/test-electron. On headless environments run it under xvfb:
//
//   xvfb-run -a pnpm sync:vscode-themes

import { mkdtempSync, mkdirSync, rmSync, writeFileSync } from "node:fs";
import { tmpdir } from "node:os";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

import { runTests } from "@vscode/test-electron";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const OUTPUT_DIR = join(ROOT, ".storybook", "themes", "generated");

// The extension-tests module that runs inside VS Code. It cycles through
// the built-in themes, opens a webview per theme, and dumps the injected
// --vscode-* variables and the "_defaultStyles" stylesheet to OUTPUT_DIR.
const SUITE_SOURCE = `
"use strict";
const fs = require("node:fs");
const path = require("node:path");
const vscode = require("vscode");

const SCRIPT_NAME = "scripts/sync-vscode-webview-themes.mjs";

const THEMES = [
	["dark", "Default Dark Modern"],
	["light", "Default Light Modern"],
	["high-contrast", "Default High Contrast"],
	["high-contrast-light", "Default High Contrast Light"],
];

const WEBVIEW_HTML = \`<!DOCTYPE html>
<html>
<body>
<script>
	const vscodeApi = acquireVsCodeApi();
	function collect() {
		const style = document.documentElement.style;
		const variables = [];
		for (let i = 0; i < style.length; i++) {
			const name = style[i];
			if (name.startsWith("--vscode-")) {
				variables.push([name, style.getPropertyValue(name)]);
			}
		}
		const defaultStyles = document.getElementById("_defaultStyles");
		return {
			variables,
			defaultStyles: defaultStyles ? defaultStyles.textContent : null,
		};
	}
	// Theme variables are injected before content loads, but poll briefly
	// in case they arrive late.
	let attempts = 0;
	const timer = setInterval(() => {
		const data = collect();
		attempts++;
		if (data.variables.length > 0 || attempts >= 50) {
			clearInterval(timer);
			vscodeApi.postMessage(data);
		}
	}, 100);
</script>
</body>
</html>\`;

function waitForThemeChange(timeoutMs) {
	return new Promise((resolvePromise) => {
		const disposable = vscode.window.onDidChangeActiveColorTheme(() => {
			disposable.dispose();
			clearTimeout(timer);
			resolvePromise();
		});
		// The event never fires if the theme is already active, so guard
		// with a timeout instead of rejecting.
		const timer = setTimeout(() => {
			disposable.dispose();
			resolvePromise();
		}, timeoutMs);
	});
}

function dumpWebview() {
	return new Promise((resolvePromise, rejectPromise) => {
		const panel = vscode.window.createWebviewPanel(
			"themeSync",
			"Theme Sync",
			vscode.ViewColumn.One,
			{ enableScripts: true },
		);
		const timer = setTimeout(() => {
			panel.dispose();
			rejectPromise(new Error("Timed out waiting for webview message"));
		}, 30_000);
		panel.webview.onDidReceiveMessage((message) => {
			clearTimeout(timer);
			panel.dispose();
			resolvePromise(message);
		});
		panel.webview.html = WEBVIEW_HTML;
	});
}

async function run() {
	const outputDir = process.env.THEME_SYNC_OUTPUT_DIR;
	if (!outputDir) {
		throw new Error("THEME_SYNC_OUTPUT_DIR is not set");
	}
	fs.mkdirSync(outputDir, { recursive: true });
	const themes = {};

	for (const [slug, themeName] of THEMES) {
		const config = vscode.workspace.getConfiguration("workbench");
		const changed = waitForThemeChange(10_000);
		await config.update(
			"colorTheme",
			themeName,
			vscode.ConfigurationTarget.Global,
		);
		await changed;

		const { variables, defaultStyles } = await dumpWebview();
		if (variables.length === 0) {
			throw new Error(\`No --vscode-* variables captured for \${themeName}\`);
		}
		variables.sort((a, b) => a[0].localeCompare(b[0]));
		themes[slug] = variables;
		console.log(\`Captured \${variables.length} variables for \${themeName}\`);

		if (slug === "dark") {
			if (!defaultStyles) {
				throw new Error("Default webview stylesheet (_defaultStyles) not found");
			}
			const header = \`/* Generated by \${SCRIPT_NAME} from VS Code \${vscode.version}. Do not edit by hand. */\\n\`;
			fs.writeFileSync(
				path.join(outputDir, "default-styles.css"),
				header + defaultStyles.trim() + "\\n",
			);
			console.log("Captured default webview stylesheet");
		}
	}

	// Minified single-line snapshot; the file is listed in .prettierignore so
	// resyncs produce a one-line diff instead of thousands of formatted lines.
	fs.writeFileSync(
		path.join(outputDir, "themes.json"),
		JSON.stringify({
			$comment: \`Generated by \${SCRIPT_NAME} from VS Code \${vscode.version}. Do not edit by hand.\`,
			vscode: vscode.version,
			themes,
		}) + "\\n",
	);
}

module.exports = { run };
`;

const FIXTURE_PACKAGE_JSON = {
	name: "theme-sync-fixture",
	publisher: "coder",
	version: "0.0.1",
	engines: { vscode: "^1.105.0" },
	main: "./extension.js",
};

const FIXTURE_EXTENSION_SOURCE = `
"use strict";
module.exports = { activate() {}, deactivate() {} };
`;

async function main() {
	const fixtureDir = mkdtempSync(join(tmpdir(), "vscode-theme-sync-"));
	try {
		writeFileSync(
			join(fixtureDir, "package.json"),
			JSON.stringify(FIXTURE_PACKAGE_JSON, null, "\t"),
		);
		writeFileSync(join(fixtureDir, "extension.js"), FIXTURE_EXTENSION_SOURCE);
		writeFileSync(join(fixtureDir, "suite.js"), SUITE_SOURCE);

		const userDataDir = join(fixtureDir, "user-data");
		mkdirSync(userDataDir);
		mkdirSync(OUTPUT_DIR, { recursive: true });

		await runTests({
			extensionDevelopmentPath: fixtureDir,
			extensionTestsPath: join(fixtureDir, "suite.js"),
			extensionTestsEnv: { THEME_SYNC_OUTPUT_DIR: OUTPUT_DIR },
			launchArgs: [
				"--disable-extensions",
				"--disable-gpu",
				"--user-data-dir",
				userDataDir,
			],
		});
		console.log(`Snapshots written to ${OUTPUT_DIR}`);
	} finally {
		rmSync(fixtureDir, { recursive: true, force: true });
	}
}

main().catch((error) => {
	console.error(error);
	process.exit(1);
});
